function normalizeName(string $filename): string {
	$camelCase = Casing::toCamel($filename);

	if (Regex::matches(
		'/^\d/',
		$camelCase
	)) {
		return "_$camelCase";
	}

	return $camelCase;
}

$iconStub =
	"\tpublic static function %2\$s(): SvgIconData\n\t{\n\t\treturn new SvgIconData('%1\$s', <<<SVG\n%3\$s\nSVG\n\t\t);\n\t}\n";
$classStub = <<<EOF
<?php
declare(strict_types=1);

namespace Elephox\Templar;

/**
 * DO NOT EDIT THIS FILE MANUALLY, YOUR CHANGES WILL BE OVERWRITTEN.
 *
 * @generated using tools/generate-icons.php
 */
abstract class %s {
%s
}
EOF;

$tmpFolder = Path::join(
	$root,
	'tmp'
);
if (!is_dir($tmpFolder)) {
	mkdir($tmpFolder);
}
chdir($tmpFolder);

foreach ($svgSources as $options) {
	$repo = $options['repo'];
	$name = md5($repo);
	$branch = $options['branch'];
	$className = $options['className'];
	$workingDir = Path::join(
		$tmpFolder,
		$name
	);
	$sourceFolder = Path::join(
		$workingDir,
		$options['icon-folder']
	);
	$destFile = Path::join(
		$root,
		'src',
		$className . '.php'
	);

	if (is_dir($workingDir)) {
		chdir($workingDir);
		echo "Pulling $repo on branch $branch" . PHP_EOL;
		shell_exec("git fetch");
		shell_exec("git switch -C $branch");
		chdir($tmpFolder);
	} else {
		echo "Cloning $repo on branch $branch" . PHP_EOL;
		shell_exec("git clone $repo $name -b $branch");
	}

	$icons = (new ElephoxDirectory($sourceFolder))->getFiles()->select(
		fn (File $file) => [
			$file->getNameWithoutExtension(),
			normalizeName($file->getNameWithoutExtension()),
			$file->getContents(),
		]
	);

	if (isset($options['filter'])) {
		$icons = $icons->where(
			fn (array $pair) => $options['filter'](
				$pair[0],
				$pair[1],
				$pair[2]
			)
		);
	}

	if (isset($options['transformer'])) {
		$icons = $icons->select(
			fn (array $pair) => [$pair[0], $pair[1], $options['transformer']($pair[2])]
		);
	}

	$stub = $options['iconStub'] ?? $iconStub;
	$icons = $icons->select(
		fn (array $pair) => sprintf(
			$stub,
			$pair[0],
			$pair[1],
			$pair[2]
		)
	)->toArray();
	$class = sprintf(
		$options['classStub'] ?? $classStub,
		$className,
		implode(
			"\n",
			$icons
		)
	);

	echo "Writing icons to $destFile" . PHP_EOL;
	file_put_contents(
		$destFile,
		$class
	);
}
