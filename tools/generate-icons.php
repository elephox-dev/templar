	[
		'repo' => 'https://github.com/twbs/icons',
		'icon-folder' => 'icons',
		'branch' => 'main',
		'className' => 'BootstrapIcon',
		'transformer' => static function (string $s): string {
			return str_replace(
				['width="16" ', 'height="16" '],
				[''],
				$s
			);
		},
	],
	[
		'repo' => 'https://github.com/primer/octicons',
		'icon-folder' => 'icons',
		'branch' => 'main',
		'className' => 'Octicon',
		'transformer' => static function (string $s): string {
			return str_replace(
				[
					'width="16" ',
					'height="16" ',
					'width="12" ',
					'height="12" ',
					'width="24" ',
					'height="24" ',
					'width="48" ',
					'height="48" ',
					'width="96" ',
					'height="96" ',
					'viewBox="',
				],
				[
